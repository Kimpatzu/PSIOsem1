package objectss;

public class Indeks {
    int i;
    int j;

    public Indeks(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getJ() {
        return j;
    }

    public void setJ(int j) {
        this.j = j;
    }

    public String toString(){
        return "i: "+j+" j: "+i;
    }
}
